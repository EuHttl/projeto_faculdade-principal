        <br>
        <li>Foreach: O foreach ele serve para percorrer por uma array ou por um objeto. </li>
        <br>
        <li>Echo: O echo serve para exibir a informação na tela para o usuário. </li>
        <br>
        <li>Exit: O exit faz com que pare um scrip. </li>
